
#calcula area do quadrado
def area(lado):
    quadrado_formula = lado * lado
    return quadrado_formula 
lado = float(input("valor do lado para area: "))
quadrado_resultado = area(lado)
print(quadrado_resultado)

#calcula perimetro do quadrado
def perimetro(lado):
    perimetro_formula = lado * 4
    return perimetro_formula
lado = float(input("valor do lado para perimetro: "))
perimetro_resultado = perimetro(lado)
print(perimetro_resultado)
